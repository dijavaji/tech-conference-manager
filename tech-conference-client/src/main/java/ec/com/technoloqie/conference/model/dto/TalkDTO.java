package ec.com.technoloqie.conference.model.dto;


public class TalkDTO {
	
	private Integer id;

	private String name;
	
	private Integer timeTalk;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getTimeTalk() {
		return timeTalk;
	}

	public void setTimeTalk(Integer timeTalk) {
		this.timeTalk = timeTalk;
	}
	

}
